function mouseOver() {
    document.getElementById('circle-one').style.backgroundColor = "#E2F89C";
    document.getElementById('circle-two').style.backgroundColor = "#E2F89C";
    document.getElementById('circle-three').style.backgroundColor = "#E2F89C";
    document.getElementById('circle-four').style.backgroundColor = "#E2F89C";
}

function mouseOut() {
    document.getElementById('circle-one').style.backgroundColor = "#C2E6F5";
    document.getElementById('circle-two').style.backgroundColor = "#C2E6F5";
    document.getElementById('circle-three').style.backgroundColor = "#C2E6F5";
    document.getElementById('circle-four').style.backgroundColor = "#C2E6F5";
}