function mouseOver() {
    document.getElementById('circle-one').style.backgroundColor = "#813405";
    document.getElementById('circle-two').style.backgroundColor = "#813405";
    document.getElementById('circle-three').style.backgroundColor = "#813405";
    document.getElementById('circle-four').style.backgroundColor = "#813405";
}

function mouseOut() {
    document.getElementById('circle-one').style.backgroundColor = "#9ED0E6";
    document.getElementById('circle-two').style.backgroundColor = "#9ED0E6";
    document.getElementById('circle-three').style.backgroundColor = "#9ED0E6";
    document.getElementById('circle-four').style.backgroundColor = "#9ED0E6";
}