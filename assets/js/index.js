function mouseOver() {
    document.getElementById('circle-one').style.backgroundColor = "blue";
    document.getElementById('circle-two').style.backgroundColor = "blue";
    document.getElementById('circle-three').style.backgroundColor = "blue";
    document.getElementById('circle-four').style.backgroundColor = "blue";
}

function mouseOut() {
    document.getElementById('circle-one').style.backgroundColor = "#9CFFFA";
    document.getElementById('circle-two').style.backgroundColor = "#9CFFFA";
    document.getElementById('circle-three').style.backgroundColor = "#9CFFFA";
    document.getElementById('circle-four').style.backgroundColor = "#9CFFFA";
}